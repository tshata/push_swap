#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t n)
{
	size_t j;

	if (s1 && s2)
	{
		j = ft_strncmp(s1, s2, n);
		if (j == 0)
			return (1);
	}
	return (0);
}
